void Engine::createUniformBuffer() {
	//动态
	static int UNIFORM_INSTANCE_COUNT = 20000;
	VkPhysicalDeviceProperties properties;
	vkGetPhysicalDeviceProperties(physicalDevice, &properties);
	size_t minUboAlignment = properties.limits.minUniformBufferOffsetAlignment;
	dynamicAlignment = sizeof(DynamicBufferObject);
	if (minUboAlignment > 0) {
		dynamicAlignment = (dynamicAlignment + minUboAlignment - 1) & ~(minUboAlignment - 1);
	}
	VkDeviceSize bufferSize = UNIFORM_INSTANCE_COUNT * dynamicAlignment;
	uboDynamic.data = (DynamicBufferObject*)_aligned_malloc(bufferSize, dynamicAlignment);
	

	dynamicUniformData.resize(swapChainImages.size());

	//normal
	normalUBOAlignment = sizeof(UniformBufferObject);
	if (minUboAlignment > 0) {
		normalUBOAlignment = (normalUBOAlignment + minUboAlignment - 1) & ~(minUboAlignment - 1);
	}
	bufferSize += normalUBOAlignment;
	//VkDeviceSize bufferSize = sizeof(UniformBufferObject);

	uniformBuffers.resize(swapChainImages.size());
	uniformBuffersMemory.resize(swapChainImages.size());

	for (size_t i = 0; i < swapChainImages.size(); i++) {
		createBuffer(bufferSize, VK_BUFFER_USAGE_UNIFORM_BUFFER_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, uniformBuffers[i], uniformBuffersMemory[i]);

		vkMapMemory(device, uniformBuffersMemory[i], 0, UNIFORM_INSTANCE_COUNT * dynamicAlignment, 0, &dynamicUniformData[i]);
	}
}
